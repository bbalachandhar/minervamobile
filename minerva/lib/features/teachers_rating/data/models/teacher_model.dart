class Teacher {
  final String name;
  final String email;
  final String contact;
  final String rating;
  final String comment;
  final bool isClassTeacher;
  final String staffId;

  Teacher({
    required this.name,
    required this.email,
    required this.contact,
    required this.rating,
    required this.comment,
    required this.isClassTeacher,
    required this.staffId,
  });
}
